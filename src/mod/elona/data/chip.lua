local function make_chip_group(chips, group)
   for _, chip in ipairs(chips) do
      local name = chip._id
      chip._id = group .. "_" .. chip._id
      chip._type = "base.chip"
      chip.group = group
      chip.image = string.format("mod/elona/graphic/%s/%s.png", group, name)
      data:add(chip)
   end
end

